    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed

        try {

            com.portaria.entity.Pessoa altP = new com.portaria.entity.Pessoa();
            
            altP.setIdpessoa(iKey);
            altP.setCpf(cpfField.getText());
            altP.setNome(nomeField.getText());
            altP.setRg(rgField.getText());

            if (iKey == 0) {

                entityManager.persist(altP);                
                list.add(altP);
                int row = list.size() - 1;
                masterTable.setRowSelectionInterval(row, row);
                masterTable.scrollRectToVisible(masterTable.getCellRect(row, 0, true));
            } else {

                altP = entityManager.merge(altP);
                int row = masterTable.getSelectedRow();
                
                list.remove(row);
                list.add(row, altP);

            }
             
            entityManager.getTransaction().commit();
            entityManager.getTransaction().begin();
            JOptionPane.showMessageDialog(null, "Operação executada com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } catch (RollbackException rex) {
            
            JOptionPane.showMessageDialog(null, "Erro na criação do Registro!", "Erro", JOptionPane.ERROR_MESSAGE);
            //rex.printStackTrace();
            entityManager.getTransaction().begin();
            java.util.Collection data = query.getResultList();
            for (Object entity : data) {
                entityManager.refresh(entity);
            }
            list.clear();
            list.addAll(data);
            /*
            entityManager.getTransaction().begin();
            List<com.portaria.model.Pessoa> merged = new ArrayList<com.portaria.model.Pessoa>(list.size());
            for (com.portaria.model.Pessoa p : list) {
                merged.add(entityManager.merge(p));
            }
            list.clear();
            list.addAll(merged);            
            */

        }

        this.enableForm(false);

    }//GEN-LAST:event_saveButtonActionPerformed

